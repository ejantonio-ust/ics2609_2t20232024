<?php
// Function 1: No parameters and no return value
function function1() {
    echo "This function has no parameters and no return value.";
}

// Function 2: Parameters but no return value
function function2($param1, $param2) {
    echo "This function has parameters, but no return value. Parameters: $param1, $param2";
}

// Function 3: Parameters and return value
function function3($a, $b) {
    return $a + $b;
}
?>
